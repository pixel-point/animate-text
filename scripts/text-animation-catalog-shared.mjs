function collectExistingFiles(root) {
  try {
    const entries = readdirSync(root, { withFileTypes: true });
    const files = [];

    for (const entry of entries) {
      const fullPath = join(root, entry.name);

      if (entry.isDirectory()) {
        for (const nested of collectExistingFiles(fullPath)) {
          files.push(join(entry.name, nested));
        }
      } else {
        files.push(entry.name);
      }
    }

    return files;
  } catch {
    return [];
  }
}

export function writeArtifacts(artifacts) {
  const roots = new Set(artifacts.map((artifact) => artifact.root));

  for (const root of roots) {
    rmSync(root, { recursive: true, force: true });
    mkdirSync(root, { recursive: true });
  }

  for (const artifact of artifacts) {
    writeFile(join(artifact.root, artifact.relativePath), artifact.content);
  }
}

export function formatArtifacts(artifacts) {
  const tempRoot = mkdtempSync(join(tmpdir(), 'animate-text-artifacts-'));

  try {
    const filesToFormat = [];
    const tempArtifacts = artifacts.map((artifact, index) => {
      const bucket = artifact.root === skillRoot ? 'skill' : 'app';
      const tempPath = join(tempRoot, bucket, artifact.relativePath);

      writeFile(tempPath, artifact.content);
      filesToFormat.push(tempPath);

      return {
        ...artifact,
        tempPath,
        index,
      };
    });

    const formatResult = spawnSync('pnpm', ['exec', 'oxfmt', ...filesToFormat], {
      cwd: repoRoot,
      encoding: 'utf8',
    });

    if (formatResult.status !== 0) {
      throw new Error(formatResult.stderr.trim() || formatResult.stdout.trim() || 'oxfmt failed.');
    }

    return tempArtifacts.map(({ tempPath, ...artifact }) => ({
      ...artifact,
      content: readText(tempPath),
    }));
  } finally {
    rmSync(tempRoot, { recursive: true, force: true });
  }
}

export function assertGeneratedOutputsMatch(artifacts) {
  const expectedByRoot = new Map();

  for (const artifact of artifacts) {
    if (!expectedByRoot.has(artifact.root)) {
      expectedByRoot.set(artifact.root, new Map());
    }

    expectedByRoot.get(artifact.root).set(artifact.relativePath, artifact.content);
  }

  const mismatches = [];

  for (const [root, expectedFiles] of expectedByRoot.entries()) {
    const existingFiles = new Set(collectExistingFiles(root));

    for (const relativePath of expectedFiles.keys()) {
      if (!existingFiles.has(relativePath)) {
        mismatches.push(`Missing generated file: ${relative(root, join(root, relativePath))}`);
        continue;
      }

      const currentContent = readText(join(root, relativePath));

      if (currentContent !== expectedFiles.get(relativePath)) {
        mismatches.push(`Stale generated file: ${relative(root, join(root, relativePath))}`);
      }
    }

    for (const existingFile of existingFiles) {
      if (!expectedFiles.has(existingFile)) {
        mismatches.push(`Unexpected generated file: ${relative(root, join(root, existingFile))}`);
      }
    }
  }

  if (mismatches.length > 0) {
    throw new Error(mismatches.join('\n'));
  }
}
