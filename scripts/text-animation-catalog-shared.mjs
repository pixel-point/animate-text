function stripBom(text) {
  return text.charCodeAt(0) === 0xfeff ? text.slice(1) : text;
}

function readText(filePath) {
  return stripBom(readFileSync(filePath, 'utf8'));
}

function readJson(filePath) {
  return JSON.parse(readText(filePath));
}

function writeFile(filePath, content) {
  mkdirSync(dirname(filePath), { recursive: true });
  writeFileSync(filePath, content, 'utf8');
}

function toJson(value) {
  return `${JSON.stringify(value, null, 2)}\n`;
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function renderTemplate(templateName, replacements) {
  let rendered = readText(join(templateRoot, templateName));

  for (const [key, value] of Object.entries(replacements)) {
    rendered = rendered.replace(new RegExp(`{{${escapeRegExp(key)}}}`, 'g'), value);
  }

  return rendered;
}

function formatIdList(ids) {
  if (ids.length === 0) {
    return 'None.';
  }

  return ids.map((id) => `- \`${id}\``).join('\n');
}

function formatSpecList(specs, visibleSet) {
  if (specs.length === 0) {
    return 'None.';
  }

  return specs
    .map((spec) => {
      const parts = [`- \`${spec.id}\``, `(${spec.target})`, `${spec.description}`];

      const extras = [];

      if (spec.renderer) {
        extras.push(`renderer: \`${spec.renderer}\``);
      }

      if (!visibleSet.has(spec.id)) {
        extras.push('website: hidden');
      }

      if (extras.length > 0) {
        parts.push(`(${extras.join(', ')})`);
      }

      return parts.join(' ');
    })
    .join('\n');
}

function orderIds(allIds, visibleIds) {
  const visibleOrder = new Map(visibleIds.map((id, index) => [id, index]));

  return [...allIds].sort((left, right) => {
    const leftVisible = visibleOrder.has(left);
    const rightVisible = visibleOrder.has(right);

    if (leftVisible && rightVisible) {
      return visibleOrder.get(left) - visibleOrder.get(right);
    }

    if (leftVisible) {
      return -1;
    }

    if (rightVisible) {
      return 1;
    }

    return left.localeCompare(right);
  });
}

